#include <iostream>
#include <string>

/* 
Data type

*/
using namespace std;
 
int main () {
  // Creating variables
  int intNum = 45;               // Integer (whole number)
  float floatNum = 65.24;     // Floating point number
  double doubleNum = 12.9456;   // Floating point number
  char myLetter = 'B';         // Character
  bool myBoolean = true;       // Boolean
  string myString = "Mayur";   // String
   
  // Print variable values
  cout << "int: " << intNum << "\n";
  cout << "float: " << floatNum << "\n";
  cout << "double: " << doubleNum << "\n";
  cout << "char: " << myLetter << "\n";
  cout << "bool: " << myBoolean << "\n";
  cout << "string: " << myString << "\n";
 
  return 0;
}